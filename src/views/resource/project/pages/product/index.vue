<script>
import api from "@/api";
import ProductEdit from "./edit.vue";
import Module from "./components/module/module.vue";
import ModuleEdit from "./components/module/module_edit.vue";
import ModuleTemplate from "./components/module/module_template.vue";

export default {
  name: "project-product",
  components: {
    ProductEdit,
    Module,
    ModuleEdit,
    ModuleTemplate,
  },
  data() {
    return {
      //
      currentSelectedRow: 0,
      // 新建设备
      showCreateCountInput: false,
      showCreateDeviceDialog: false,
      // new device
      newDevice: {
        productId: this.$route.params.productId,
        name: "新设备",
        location: "0,0",
        industry: "厂家",
        model: "型号",
        createCount: 1,
        description: "产品描述",
      },
      //1 量产；2 单个
      createType: 2,
      createTypes: [
        { label: "批量生产", value: 1 },
        { label: "单个设备", value: 2 },
      ],
      //
      //-------
      id: 0, //projectId
      stat: {
        all: 0,
        dev: 0,
        run: 0,
        lock: 0,
      },
      product: {
        list: [],
        select: "0",
        key: "",
        current: 1,
        size: 10,
        total: 0,
        loading: false,
      },
      detail: {
        data: {},
        visible: false,
        rules: {},
      },
      module: {
        productId: 0,
        data: [],
        current: 1,
        size: 10,
        total: 0,
        visible: false,
      },
      rules: {
        name: [
          {
            required: true,
            message: "内容不可为空",
            trigger: "blur",
          },
        ],
        productId: [
          {
            required: true,
            message: "内容不可为空",
            trigger: "blur",
          },
        ],
      },
    };
  },
  mounted() {
    this.id = this.$route.params.projectId;
    this.productList();
  },
  methods: {
    //
    gotoDetailPage(id) {
      this.$router.push("/product/" + id + "/detail");
    },
    //创建设备
    createDevice() {
      this.$refs.newDeviceForm.validate((valid) => {
        if (valid) {
          this.newDevice.productId = this.id;
          this.createDeviceAPI();
        }
      });
    },
    createDeviceAPI() {
      let _this = this;
      console.log(this.newDevice);
      this.$api
        .DEVICE_CREATE(this.newDevice)
        .then((res) => {
          if (res.id > 0) {
            this.$message({
              message: "设备创建成功",
              type: "success",
            });
            _this.showCreateDeviceDialog = false;
          } else {
            this.$message({
              message: "设备创建失败",
              type: "error",
            });
          }
        })
        .catch((e) => {
          this.$message({
            message: "设备创建失败",
            type: "error",
          });
        });
    },
    //
    onCreateTypeChange(createType) {
      console.log(createType);
      if (createType == 1) {
        this.showCreateCountInput = true;
      }
      if (createType == 2) {
        this.showCreateCountInput = false;
      }
    },
    handleCreateCountChange(createCount) {
      this.newDevice.createCount = createCount;
    },
    ///-------------------------------------------------
    productList() {
      const that = this;
      const params = {
        projectId: that.id,
        current: that.product.current,
        size: that.product.size,
      };
      that.product.loading = true;
      api
        .PRODUCT_LIST(params)
        .then((res) => {
          that.product.list = res.records;
          that.product.total = res.total;
          that.product.loading = false;
        })
        .catch((err) => {
          that.product.loading = false;
        });
    },
    newProduct() {
      this.detail.data = {
        name: "",
        type: "1",
        logo: "",
        tags: "",
        protocol: 0,
        parameters: [],
        description: "",
      };
      this.detail.visible = true;
    },
    editProduct(item) {
      if (item.type != 1) {
        item.type = "";
      }
      const data = { ...item, parameters: [...item.parameters] };
      this.detail.data = data;
      this.detail.visible = true;
    },
    editSubmit(item) {
      const that = this;
      const id = item.id;
      const data = {
        projectId: that.id,
        name: item.name,
        type: item.type,
        logo: item.logo,
        tags: item.tags,
        protocol: item.protocol,
        parameters: item.parameters,
        description: item.description,
      };
      if (id == undefined) {
        api.PRODUCT_CREATE(data).then((res) => {
          that.detail.visible = false;
          that.productList();
        });
        return;
      }
      api.PRODUCT_UPDATE(id, data).then((res) => {
        that.detail.visible = false;
        that.productList();
      });
    },
    // 打开产品模块
    openModule(id) {
      const that = this;
      const params = {
        productId: id,
        current: that.module.current,
        size: that.module.size,
      };
      that.loading = true;
      this.$api
        .MODULE_LIST(params)
        .then((res) => {
          that.module.data = res.records;
          that.module.total = res.total;
          that.module.visible = true;
          that.module.productId = id;
          that.loading = false;
        })
        .catch((err) => {
          console.log(err);
          that.loading = false;
        });
    },
  },
};
</script>

